// Ïä§Ìã∞ÌÇ§ Ìó§Îçî delegate ÌÅ¥ÎûòÏä§
class _StickyFilterBarDelegate extends SliverPersistentHeaderDelegate {
  final double minHeight;
  final double maxHeight;
  final Widget child;

  _StickyFilterBarDelegate({
    required this.minHeight,
    required this.maxHeight,
    required this.child,
  });

  @override
  double get minExtent => minHeight;

  @override
  double get maxExtent => maxHeight;

  @override
  Widget build(
    BuildContext context,
    double shrinkOffset,
    bool overlapsContent,
  ) {
    // Ïä§ÌÅ¨Î°§Ïóê Îî∞Îùº Î∞∞Í≤ΩÏóê Í∑∏Î¶ºÏûê Ìö®Í≥ºÎ•º Ï∂îÍ∞ÄÌïòÏó¨ Íµ¨Î∂ÑÍ∞ê Ìñ•ÏÉÅ
    return Container(
      decoration: BoxDecoration(
        color: Colors.white,
        boxShadow:
            overlapsContent
                ? [
                  BoxShadow(
                    color: Colors.black.withAlpha(0x0D),
                    blurRadius: 8,
                    offset: const Offset(0, 4),
                  ),
                ]
                : [],
      ),
      child: child,
    );
  }

  @override
  bool shouldRebuild(_StickyFilterBarDelegate oldDelegate) {
    return maxHeight != oldDelegate.maxHeight ||
        minHeight != oldDelegate.minHeight ||
        child != oldDelegate.child;
  }
}
